print("DADOS TRES NUMEROS DECIR SI LA SUMA DE LOS DOS PRIMEROS ES IGUAL AL TERCERO")
print("---------------------------------------------------------------------------")

numA = int(input("Ingresar el número a: "))
numB = int(input("Ingresar el número b: "))
numC = int(input("Ingresar el número c: "))

if numA + numB == numC:
    print("-------------------------------------------------")
    print("LA SUMA DE LOS NúMEROS a Y b ES IGUAL AL NÚMERO c")
    print("-------------------------------------------------")
else:
    print("----------------------------------------------------")
    print("LA SUMA DE LOS NúMEROS a Y b NO ES IGUAL AL NÚMERO c")
    print("----------------------------------------------------")
